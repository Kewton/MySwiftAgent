echo "ğŸ“ Setting up worktree: $WORKTREE_NAME"

# .env.local ãŒæ—¢ã«å­˜åœ¨ã™ã‚‹å ´åˆã¯å†å®Ÿè¡Œã—ãªã„
if [ -f .env.local ]; then
    echo "âš ï¸  .env.local already exists. Skipping setup."
    echo "   If you want to regenerate, delete .env.local first:"
    echo "   rm .env.local && ~/MySwiftAgent/scripts/setup-worktree.sh"
    exit 0
fi

# å…¨worktreeã‹ã‚‰ä½¿ç”¨ä¸­ã®ã‚¤ãƒ³ãƒ‡ãƒƒã‚¯ã‚¹ã‚’åé›†
echo "ğŸ” Detecting used worktree indices..."
USED_INDICES=()

# MySwiftAgent-worktrees ãƒ‡ã‚£ãƒ¬ã‚¯ãƒˆãƒªå†…ã®å…¨worktreeã‚’ã‚¹ã‚­ãƒ£ãƒ³
WORKTREES_DIR="$(dirname "$WORKTREE_DIR")"
if [ -d "$WORKTREES_DIR" ]; then
    for worktree in "$WORKTREES_DIR"/*/; do
        if [ -f "${worktree}.env.local" ]; then
            # .env.local ã‹ã‚‰ WORKTREE_INDEX ã‚’æŠ½å‡º
            INDEX=$(grep "^WORKTREE_INDEX=" "${worktree}.env.local" 2>/dev/null | cut -d'=' -f2)
            if [ -n "$INDEX" ]; then
                USED_INDICES+=($INDEX)
            fi
        fi
    done
fi

# ä½¿ç”¨ä¸­ã®ã‚¤ãƒ³ãƒ‡ãƒƒã‚¯ã‚¹ã‚’ã‚½ãƒ¼ãƒˆ
if [ ${#USED_INDICES[@]} -gt 0 ]; then
    USED_INDICES=($(printf '%s\n' "${USED_INDICES[@]}" | sort -n | uniq))
fi

# ç©ºã„ã¦ã„ã‚‹æœ€å°ã®ã‚¤ãƒ³ãƒ‡ãƒƒã‚¯ã‚¹ã‚’æ¤œç´¢ï¼ˆ1ã‹ã‚‰é †ã«ï¼‰
WORKTREE_INDEX=1
for used in "${USED_INDICES[@]}"; do
    if [ "$WORKTREE_INDEX" -eq "$used" ]; then
        WORKTREE_INDEX=$((WORKTREE_INDEX + 1))
    else
        break
    fi
done

echo "âœ… Assigned index: $WORKTREE_INDEX"
if [ ${#USED_INDICES[@]} -gt 0 ]; then
    echo "   Used indices: ${USED_INDICES[@]}"
else
    echo "   Used indices: none (this is the first worktree)"
fi

# ãƒãƒ¼ãƒˆç•ªå·ã‚’è¨ˆç®—
BASE_EXPERTAGENT=8104
BASE_MYVAULT=8103
BASE_MYSCHEDULER=8102
