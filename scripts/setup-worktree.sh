EOF

echo ""
echo "âœ… Created .env.local with port assignments:"
echo "   - expertAgent: $PORT_EXPERTAGENT"
echo "   - myVault: $PORT_MYVAULT"
echo "   - myscheduler: $PORT_MYSCHEDULER"
echo "   - jobqueue: $PORT_JOBQUEUE"
echo "   - graphAiServer: $PORT_GRAPHAI"
