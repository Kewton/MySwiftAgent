# scripts/setup-worktree.sh
# Worktreeè‡ªå‹•ã‚»ãƒƒãƒˆã‚¢ãƒƒãƒ—ã‚¹ã‚¯ãƒªãƒ—ãƒˆï¼ˆç©ºããƒãƒ¼ãƒˆæ¤œå‡ºæ–¹å¼ï¼‰
